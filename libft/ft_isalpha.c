#include "libft.h"

/*
** checks if character is alphabetic
*/

int		ft_isalpha(int c)
{
	if ((c >= 'A') && (c <= 'Z'))
		return (1);
	else if ((c >= 'a') && (c <= 'z'))
		return (1);
	return (0);
}
